/**
 * Pattern-based permission classifier.
 *
 * Evaluates tool names against allow/deny/ask pattern rules with
 * named group expansion. Groups are expanded at construction time
 * (compile-once, validate-per-call).
 */

import type { ApprovalDecision, ApprovalRequest } from "@koi/core/middleware";
import type { PermissionBackend, PermissionDecision } from "@koi/core/permission-backend";

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export interface PermissionRules {
  readonly allow: readonly string[];
  readonly deny: readonly string[];
  readonly ask: readonly string[];
}

export interface PatternBackendConfig {
  /** Tool name patterns for allow/deny/ask decisions. */
  readonly rules: PermissionRules;
  /** Deny unmatched tools. Default: true (fail-closed). */
  readonly defaultDeny?: boolean;
  /** Named tool group expansions. Use `group:<name>` in rules. */
  readonly groups?: Readonly<Record<string, readonly string[]>>;
}

// ---------------------------------------------------------------------------
// Default groups
// ---------------------------------------------------------------------------

