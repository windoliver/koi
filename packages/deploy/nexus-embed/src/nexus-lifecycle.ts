/**
 * Scaffolds `nexus.yaml` and supporting files for the Nexus Docker stack.
 *
 * Generates config directly in TypeScript — no external `nexus` CLI dependency.
 * Each workspace gets a deterministic port derived from its path and a
 * workspace-local `nexus-data/` directory for isolation.
 *
 * Maps Koi preset IDs to Nexus preset IDs:
 * - local → local, demo → demo, mesh → shared
 */
export async function nexusInit(
  koiPreset: string,
  options?: NexusInitOptions | undefined,
): Promise<Result<void, KoiError>> {
  const cwd = options?.cwd ?? process.cwd();
  const verbose = options?.verbose ?? false;

  try {
    generateNexusConfig({
      koiPreset,
      cwd,
      port: options?.port,
      channel: options?.channel,
      verbose,
    });
    return { ok: true, value: undefined };
  } catch (err: unknown) {
    return {
      ok: false,
      error: {
        code: "EXTERNAL" as const,
        message: `Failed to generate nexus.yaml: ${err instanceof Error ? err.message : String(err)}`,
        retryable: false,
        context: { cwd },
      },
    };
  }
}

/**
 * Starts the Nexus Docker Compose stack via `nexus up`.
 *
 * **Important**: Only call this for Docker-backed presets (demo, mesh/shared).
 * The `local` preset's `nexus up` exits with code 0 without starting services.
 * For local/embed-lite mode, use `ensureNexusRunning()` instead.
 *
 * If `nexus.yaml` doesn't exist in the working directory, auto-runs
 * `nexus init --preset <koiPreset>` first.
 *
 * After startup, reads `nexus.yaml` to extract the actual HTTP port
 * (which may differ from the default if ports were auto-shifted during
 * conflict resolution).
 */
export async function nexusUp(
  options?: NexusLifecycleOptions & {
    /** Koi preset for auto-init when nexus.yaml is missing. Default: "demo". */
    readonly koiPreset?: string | undefined;
    /** Host override. Default: "127.0.0.1". */
    readonly host?: string | undefined;
    /** Build images from source (passes --build and --compose-file to `nexus up`). Requires `sourceDir` pointing to the nexus repo root. */
    readonly build?: boolean | undefined;
    /** Path to the nexus repo root for --build (derives compose file as `<sourceDir>/docker-compose.yml`). */
    readonly sourceDir?: string | undefined;
    /** Port conflict resolution strategy (passes --port-strategy to `nexus up`). Default: "auto". */
    readonly portStrategy?: "auto" | "prompt" | "fail" | undefined;
    /** Override the HTTP port (passed to `nexus init` during auto-init). */
    readonly port?: number | undefined;
    /** Docker image channel for auto-init: "stable" or "edge". Default: "edge". */
    readonly channel?: "stable" | "edge" | undefined;
  },
): Promise<Result<NexusUpResult, KoiError>> {
  const cwd = options?.cwd ?? process.cwd();
  const verbose = options?.verbose ?? false;
  const host = options?.host ?? DEFAULT_HOST;
  const sourceDir = options?.sourceDir;

  const binaryCheck = await ensureBinary(sourceDir);
  if (!binaryCheck.ok) {
    // Fallback: if nexus CLI is unavailable but nexus.yaml + compose_file exist,
    // run docker compose directly. This handles environments where `uv` or `nexus`
    // isn't installed but Docker is available and the stack was previously initialized.
    const fallbackResult = await dockerComposeFallback(cwd, verbose, host);
    if (fallbackResult !== undefined) return fallbackResult;
    return binaryCheck;
  }

  // Auto-init if no config file exists (nexus.yaml or nexus.yml)
  let autoInitialized = false;
  let configPath = findNexusConfig(cwd);
  if (configPath === undefined) {
    const koiPreset = options?.koiPreset ?? "demo";
    if (verbose) {
      process.stderr.write(
        `Nexus: nexus.yaml not found, running nexus init --preset ${koiPreset}\n`,
      );
    }
    const initResult = await nexusInit(koiPreset, {
      cwd,
      verbose,
      sourceDir,
      port: options?.port,
      channel: options?.channel,
    });
    if (!initResult.ok) return initResult;
    autoInitialized = true;
    configPath = findNexusConfig(cwd) ?? join(cwd, "nexus.yaml");
  }

  // Always validate data_dir points to ${cwd}/nexus-data — even when nexus.yaml
  // pre-exists. A stale nexus.yaml (e.g., copied from another worktree or symlinked
  // from ~/.koi/demo/) may point to a different worktree's data dir, causing
  // cross-worktree contamination and Nexus write failures.
  patchNexusDataDir(cwd, verbose);

  // Clear NEXUS_API_KEY from the environment before starting Docker.
  // The nexus.yaml api_key uses nx_admin_* format which the database auth
  // provider rejects (requires sk-* prefix). By clearing it, the container
  // entrypoint auto-generates a proper sk-* admin key.
  const savedApiKey = process.env.NEXUS_API_KEY;
  delete process.env.NEXUS_API_KEY;

  // Fast resume: if .state.json exists from a previous run, use `nexus start`
  // instead of `nexus up`. `nexus start` resumes paused containers without
  // pulling images or re-resolving ports — much faster for repeat startups.
  // Only fall through to `nexus up` for first-run or when --build is requested.
  const existingState = readRuntimeState(cwd);
  const canFastResume = existingState !== undefined && options?.build !== true && !autoInitialized;

  let upResult: Result<void, KoiError>;
  if (canFastResume) {
    if (verbose) {
      process.stderr.write(
        `Nexus: reusing existing instance at http://${host}:${String(existingState.ports.http)}\n`,
      );
    }
    upResult = await runNexusCommand(["start"], cwd, verbose, "nexus start", sourceDir);
    // If start fails (e.g. containers were killed/removed), clean up stale
    // containers before full restart. Without this, `nexus up` sees the old
    // containers occupying the port and allocates a new one (#1077).
    if (!upResult.ok) {
      if (verbose) {
        process.stderr.write("Nexus: fast resume failed, cleaning up stale containers\n");
      }
      // Remove dead containers so the configured port is freed.
      // Result intentionally ignored — if down fails (already removed), up still works.
      await runNexusCommand(["down"], cwd, verbose, "nexus down", sourceDir);
      upResult = await runNexusFullUp(options, cwd, verbose, sourceDir);
    }
  } else {
    upResult = await runNexusFullUp(options, cwd, verbose, sourceDir);
  }

  // Restore the original env var (may be needed by other code)
  if (savedApiKey !== undefined) {
    process.env.NEXUS_API_KEY = savedApiKey;
  }
  if (!upResult.ok) return upResult;

  // Read connection info from .state.json (authoritative source written by
  // `nexus up` after health checks pass). Falls back to nexus.yaml if
  // .state.json is missing (e.g., first run before health check completes).
  const runtimeState = readRuntimeState(cwd);
  const yamlConfig = readNexusConfig(configPath);

  const port = runtimeState?.ports.http ?? yamlConfig.port;
  const baseUrl = `http://${host}:${String(port)}`;

  // .state.json api_key is the sk-* key written by the container entrypoint.
  // Fall back to nexus.yaml api_key (nx_admin_* format) only if .state.json
  // doesn't have one — callers should prefer the sk-* key for database auth.
  const apiKey = runtimeState?.api_key || yamlConfig.apiKey;

  // Write to shared location so other worktrees can discover the key
  if (apiKey !== undefined) {
    const { writeSharedNexusKey } = await import("./ensure-running.js");
    writeSharedNexusKey(apiKey);
  }

  return { ok: true, value: { baseUrl, apiKey, autoInitialized } };
}

/**
 * Pauses the Nexus stack via `nexus stop` (keeps containers and volumes).
 *
 * Containers are stopped but not removed. Resume with `nexus start` or
 * `nexus up` (which detects stopped containers and does a fast resume).
 * This is much faster than `nexusDown()` + `nexusUp()` because it skips
 * port resolution, image pulls, and full health checks.
 */
export async function nexusStop(options?: NexusLifecycleOptions): Promise<Result<void, KoiError>> {
  const cwd = options?.cwd ?? process.cwd();
  const verbose = options?.verbose ?? false;
  const sourceDir = options?.sourceDir;

  const binaryCheck = await ensureBinary(sourceDir);
  if (!binaryCheck.ok) return binaryCheck;

  return runNexusCommand(["stop"], cwd, verbose, "nexus stop", sourceDir);
}

/**
 * Destroys the Nexus stack via `nexus down` (removes containers).
 *
 * For normal shutdown, prefer `nexusStop()` which keeps containers for
 * fast resume. Use `nexusDown()` only for full cleanup or when volumes
 * need to be removed.
 */
export async function nexusDown(
  options?: NexusLifecycleOptions & {
    /** Also remove volumes (passes --volumes to `nexus down`). Default: false. */
    readonly volumes?: boolean | undefined;
  },
): Promise<Result<void, KoiError>> {
  const cwd = options?.cwd ?? process.cwd();
  const verbose = options?.verbose ?? false;
  const sourceDir = options?.sourceDir;

  const binaryCheck = await ensureBinary(sourceDir);
  if (!binaryCheck.ok) return binaryCheck;

  const args: string[] = ["down"];
  if (options?.volumes === true) args.push("--volumes");

  return runNexusCommand(args, cwd, verbose, "nexus down", sourceDir);
}

