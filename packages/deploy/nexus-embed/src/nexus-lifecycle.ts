/**
 * Fallback: start Nexus via `docker compose` directly when the `nexus` CLI
 * isn't available. Reads compose_file + data_dir + ports from nexus.yaml.
 * Returns undefined if fallback isn't possible (no nexus.yaml, no compose_file).
 */
async function dockerComposeFallback(
  cwd: string,
  verbose: boolean,
  host: string,
): Promise<Result<NexusUpResult, KoiError> | undefined> {
  const configPath = findNexusConfig(cwd);
  if (configPath === undefined) return undefined;

  try {
    const raw = readFileSync(configPath, "utf-8");
    const composeMatch = /^compose_file:\s*['"]?(.+?)['"]?\s*$/m.exec(raw);
    if (composeMatch?.[1] === undefined || !existsSync(composeMatch[1])) return undefined;
    const composeFile = composeMatch[1];

    // Read data_dir for volume mount
    const dataDirMatch = /^data_dir:\s*['"]?(.+?)['"]?\s*$/m.exec(raw);
    const dataDir = dataDirMatch?.[1] ?? join(cwd, "nexus-data");

    // Read profiles
    const profiles: string[] = [];
    const profileSection = /^compose_profiles:\s*\n((?:\s*-\s*.+\n)*)/m.exec(raw);
    if (profileSection?.[1] !== undefined) {
      for (const m of profileSection[1].matchAll(/^\s*-\s*(.+)/gm)) {
        if (m[1] !== undefined) profiles.push(m[1].trim());
      }
    }

    // Read port + api_key
    const config = readNexusConfig(configPath);

    if (verbose) {
      process.stderr.write(
        `Nexus: nexus CLI unavailable, falling back to docker compose\n` +
          `  compose_file: ${composeFile}\n` +
          `  data_dir: ${dataDir}\n`,
      );
    }

    // Build docker compose command with a unique project name derived from
    // the data_dir hash. This prevents conflicts when multiple worktrees
    // use the same compose_file but different data directories.
    const projectHash = createHash("md5").update(dataDir).digest("hex").slice(0, 8);
    const projectName = `nexus-${projectHash}`;
    const args = ["compose", "-f", composeFile, "-p", projectName];
    for (const p of profiles) {
      args.push("--profile", p);
    }
    args.push("up", "-d", "--wait");

    const proc = Bun.spawn(["docker", ...args], {
      cwd,
      stdin: "ignore",
      stdout: "pipe",
      stderr: "pipe",
      env: {
        ...process.env,
        NEXUS_HOST_DATA_DIR: dataDir,
        NEXUS_PORT: String(config.port),
        NEXUS_GRPC_PORT: String(config.port + 1),
        POSTGRES_PORT: String(config.port + 2),
        DRAGONFLY_PORT: String(config.port + 3),
        ZOEKT_PORT: String(config.port + 4),
        DOCKER_CLI_HINTS: "false",
        BUILDKIT_PROGRESS: "plain",
      },
    });

    const exitCode = await proc.exited;
    if (exitCode !== 0) {
      const stderr = await new Response(proc.stderr).text();
      return {
        ok: false,
        error: {
          code: "INTERNAL" as const,
          message: `docker compose up failed (exit ${String(exitCode)}): ${stderr.slice(0, 500)}`,
          retryable: false,
          context: {},
        },
      };
    }

    const baseUrl = `http://${host}:${String(config.port)}`;
    return {
      ok: true,
      value: { baseUrl, apiKey: config.apiKey, autoInitialized: false },
    };
  } catch {
    return undefined;
  }
}

/** Run full `nexus up` with all flags (pull, build, port-strategy). */
async function runNexusFullUp(
  options: Parameters<typeof nexusUp>[0],
  cwd: string,
  verbose: boolean,
  sourceDir: string | undefined,
): Promise<Result<void, KoiError>> {
  const upArgs: string[] = ["up"];
  if (options?.sourceDir !== undefined) {
    upArgs.push("--compose-file", join(options.sourceDir, "docker-compose.yml"));
  }
  if (options?.build === true) upArgs.push("--build");
  if (options?.portStrategy !== undefined) upArgs.push("--port-strategy", options.portStrategy);
  return runNexusCommand(upArgs, cwd, verbose, "nexus up", sourceDir);
}

/** Checks that the nexus binary is available on PATH. */
async function ensureBinary(sourceDir?: string | undefined): Promise<Result<void, KoiError>> {
  const binaryParts = resolveNexusBinary(sourceDir);
  const available = await checkBinaryAvailable(binaryParts);
  if (!available) {
    const binaryName = binaryParts[0] ?? "nexus";
    return {
      ok: false,
      error: {
        code: "NOT_FOUND" as const,
        message: `Cannot find '${binaryName}' on PATH. Install it:\n  - uv: pip install nexus-ai-fs\n  - Or set NEXUS_COMMAND`,
        retryable: false,
        context: { binary: binaryName },
      },
    };
  }
  return { ok: true, value: undefined };
}

/** Nexus config search paths (nexus.yaml preferred, nexus.yml fallback). */
const CONFIG_NAMES = ["nexus.yaml", "nexus.yml"] as const;

/**
 * Finds the nexus config file in the given directory.
 * Searches for nexus.yaml first, then nexus.yml.
 */
function findNexusConfig(cwd: string): string | undefined {
  for (const name of CONFIG_NAMES) {
    const candidate = join(cwd, name);
    if (existsSync(candidate)) return candidate;
  }
  return undefined;
}

/**
 * Reads the Nexus runtime state from `{data_dir}/.state.json`.
 *
 * `.state.json` is written by `nexus up` after health checks complete and
 * contains the authoritative resolved ports, API key, and project name.
 * The `data_dir` is read from `nexus.yaml` (stored as an absolute path).
 *
 * Returns `undefined` if `nexus.yaml` or `.state.json` is missing/unreadable.
 */
export function readRuntimeState(cwd: string): NexusRuntimeState | undefined {
  const configPath = findNexusConfig(cwd);
  if (configPath === undefined) return undefined;

  try {
    const raw = readFileSync(configPath, "utf-8");
    // Extract data_dir (absolute path set by `nexus init`)
    const dataDirMatch = /^data_dir:\s*['"]?(.+?)['"]?\s*$/m.exec(raw);
    if (dataDirMatch?.[1] === undefined) return undefined;

    const dataDir = dataDirMatch[1];
    const statePath = join(dataDir, STATE_JSON_FILE);
    if (!existsSync(statePath)) return undefined;

    const stateRaw = readFileSync(statePath, "utf-8");
    const parsed: unknown = JSON.parse(stateRaw);
    if (typeof parsed !== "object" || parsed === null) return undefined;

    const state = parsed as Record<string, unknown>;
    // Minimal validation: must have ports object
    if (typeof state.ports !== "object" || state.ports === null) return undefined;

    return parsed as NexusRuntimeState;
  } catch {
    return undefined;
  }
}

/** Values extracted from nexus.yaml after startup. */
interface NexusConfigValues {
  readonly port: number;
  readonly apiKey: string | undefined;
}

/**
 * Reads key values from nexus.yaml: HTTP port and API key.
 *
 * Uses simple regex extraction to avoid a YAML parser dependency.
 * nexus.yaml is materialized with explicit keys by `nexus init`,
 * so these regexes are safe.
 */
function readNexusConfig(configPath: string): NexusConfigValues {
  try {
    const raw = readFileSync(configPath, "utf-8");

    // Extract ports.http
    let port = DEFAULT_PORT;
    const portsSection = /^ports:\s*\n((?:\s+\S.*\n)*)/m.exec(raw);
    if (portsSection !== undefined && portsSection !== null) {
      const httpMatch = /^\s+http:\s*(\d+)/m.exec(portsSection[1] ?? "");
      if (httpMatch?.[1] !== undefined) {
        const parsed = Number.parseInt(httpMatch[1], 10);
        if (!Number.isNaN(parsed) && parsed > 0) port = parsed;
      }
    }

    // Extract api_key (top-level key, format: api_key: nx_admin_...)
    let apiKey: string | undefined;
    const apiKeyMatch = /^api_key:\s*['"]?(\S+?)['"]?\s*$/m.exec(raw);
    if (apiKeyMatch?.[1] !== undefined && apiKeyMatch[1].length > 0) {
      apiKey = apiKeyMatch[1];
    }

    return { port, apiKey };
  } catch {
    // File missing or unreadable — use defaults
    return { port: DEFAULT_PORT, apiKey: undefined };
  }
}

/**
 * Runs a nexus CLI subcommand and waits for it to exit.
 *
 * Always captures both stdout and stderr so that Nexus CLI errors
 * (emitted via Rich Console to stdout) are surfaced in error messages.
 * In verbose mode, output is also forwarded to inherit streams.
 */
async function runNexusCommand(
  args: readonly string[],
  cwd: string,
  verbose: boolean,
  label: string,
  sourceDir?: string | undefined,
): Promise<Result<void, KoiError>> {
  const binaryParts = resolveNexusBinary(sourceDir);
  const cmd = [...binaryParts, ...args];

  if (verbose) {
    process.stderr.write(`Nexus: running ${cmd.join(" ")}\n`);
  }

  // Read data_dir from nexus.yaml so Docker Compose mounts the correct volume.
  // The compose file uses ${NEXUS_HOST_DATA_DIR:-./nexus-data} for the data mount.
  const dataDirEnv: Record<string, string> = {};
  const configPath = findNexusConfig(cwd);
  if (configPath !== undefined) {
    try {
      const raw = readFileSync(configPath, "utf-8");
      const match = /^data_dir:\s*['"]?(.+?)['"]?\s*$/m.exec(raw);
      if (match?.[1] !== undefined) {
        dataDirEnv.NEXUS_HOST_DATA_DIR = match[1];
      }
    } catch {
      // Non-fatal — compose will use default ./nexus-data
    }
  }

  try {
    const proc = Bun.spawn(cmd, {
      cwd,
      stdin: "ignore",
      // Always pipe to capture output; forward to user in verbose mode after
      stdout: "pipe",
      stderr: "pipe",
      env: {
        ...process.env,
        ...dataDirEnv,
        // Suppress Docker Compose TTY progress bars that bypass pipe redirections
        DOCKER_CLI_HINTS: "false",
        BUILDKIT_PROGRESS: "plain",
        // Prevent Python Rich Console from writing ANSI to /dev/tty
        NO_COLOR: "1",
        TERM: "dumb",
      },
    });

    const exitCode = await proc.exited;

    // Collect output (Nexus CLI uses Rich Console on stdout for most messages)
    const stdoutText = proc.stdout !== null ? await new Response(proc.stdout).text() : "";
    const stderrText = proc.stderr !== null ? await new Response(proc.stderr).text() : "";

    // Forward output in verbose mode
    if (verbose) {
      if (stdoutText) process.stdout.write(stdoutText);
      if (stderrText) process.stderr.write(stderrText);
    }

    if (exitCode !== 0) {
      // Combine stdout + stderr for the error message since Nexus
      // prints most diagnostics to stdout via Rich Console
      const output = [stdoutText.trim(), stderrText.trim()].filter((s) => s.length > 0).join("\n");
      return {
        ok: false,
        error: {
          code: "EXTERNAL" as const,
          message: `${label} exited with code ${String(exitCode)}${output ? `:\n${output}` : ""}`,
          retryable: false,
          context: { cmd, exitCode },
        },
      };
    }

    return { ok: true, value: undefined };
  } catch (err: unknown) {
    return {
      ok: false,
      error: {
        code: "EXTERNAL" as const,
        message: `Failed to run ${label}: ${err instanceof Error ? err.message : String(err)}`,
        retryable: false,
        cause: err,
        context: { cmd },
      },
    };
  }
}
