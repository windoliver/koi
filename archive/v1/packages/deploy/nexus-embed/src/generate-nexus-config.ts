/**
 * Generate nexus.yaml and supporting files directly — no external CLI dependency.
 *
 * Replaces the `nexus init` shell-out with a pure TypeScript generator.
 * Each workspace gets a deterministic port derived from its path and
 * a workspace-local data directory for isolation.
 */

import { randomBytes } from "node:crypto";
import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import { join, resolve } from "node:path";

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export interface GenerateNexusConfigOptions {
  /** Koi preset ID (local, demo, mesh). */
  readonly koiPreset: string;
  /** Working directory where files will be written. Default: process.cwd(). */
  readonly cwd?: string | undefined;
  /** Override the HTTP port (skips derivation). */
  readonly port?: number | undefined;
  /** Docker image channel: "stable" or "edge". Default: "edge". */
  readonly channel?: "stable" | "edge" | undefined;
  /** Emit verbose diagnostics to stderr. Default: false. */
  readonly verbose?: boolean | undefined;
}

// ---------------------------------------------------------------------------
// Nexus preset mapping
// ---------------------------------------------------------------------------

/** Maps Koi preset IDs to Nexus preset IDs. */
const PRESET_MAP: Readonly<Record<string, string>> = {
  local: "local",
  demo: "demo",
  mesh: "shared",
  sqlite: "demo",
} as const;

// ---------------------------------------------------------------------------
// Port derivation
// ---------------------------------------------------------------------------

/** Ephemeral port range for derived ports. */
const PORT_RANGE_MIN = 10000;
const PORT_RANGE_MAX = 60000;
const PORT_RANGE_SIZE = PORT_RANGE_MAX - PORT_RANGE_MIN;

/**
 * Derive a stable port number from a workspace path.
 *
 * Uses SHA-256 of the resolved absolute path, takes the first 4 hex chars,
 * and maps to the ephemeral range 10000-60000. Each worktree gets a stable
 * unique port without collisions.
 */
export function derivePort(cwd: string): number {
  const absolute = resolve(cwd);
  const hasher = new Bun.CryptoHasher("sha256");
  hasher.update(absolute);
  const hex = hasher.digest("hex").slice(0, 8);
  const num = Number.parseInt(hex, 16);
  return PORT_RANGE_MIN + (num % PORT_RANGE_SIZE);
}

// ---------------------------------------------------------------------------
// API key generation
// ---------------------------------------------------------------------------

/** Generate a random API key in sk-* format (Base64URL, 43 chars). */
function generateApiKey(): string {
  const bytes = randomBytes(32);
  const base64url = bytes.toString("base64url");
  return `sk-${base64url}`;
}

