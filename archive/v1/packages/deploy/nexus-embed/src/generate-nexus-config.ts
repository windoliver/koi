/** pgvector init SQL — required by PostgreSQL Docker entrypoint. */
const PGVECTOR_INIT_SQL = `-- Enable pgvector extension for vector similarity search
CREATE EXTENSION IF NOT EXISTS vector;
`;

// ---------------------------------------------------------------------------
// Public API
// ---------------------------------------------------------------------------

/**
 * Generate nexus.yaml and supporting files directly in the workspace.
 *
 * Creates:
 * - `nexus.yaml` — Nexus configuration with workspace-local data_dir and derived port
 * - `nexus-stack.yml` — Docker Compose file for the Nexus stack
 * - `001-enable-pgvector.sql` — PostgreSQL init script for pgvector
 *
 * Does NOT overwrite existing files (check before calling).
 */
export function generateNexusConfig(options: GenerateNexusConfigOptions): void {
  const cwd = options.cwd ?? process.cwd();
  const verbose = options.verbose ?? false;
  const channel = options.channel ?? "edge";
  const nexusPreset = PRESET_MAP[options.koiPreset] ?? "demo";

  const httpPort = options.port ?? derivePort(cwd);
  const dataDir = join(resolve(cwd), "nexus-data");
  const apiKey = generateApiKey();
  const composeFilePath = join(resolve(cwd), "nexus-stack.yml");

  // Ensure data directory exists
  if (!existsSync(dataDir)) {
    mkdirSync(dataDir, { recursive: true });
  }

  // Write nexus.yaml
  const yamlPath = join(cwd, "nexus.yaml");
  const yamlContent = generateNexusYamlContent(
    nexusPreset,
    dataDir,
    httpPort,
    apiKey,
    channel,
    composeFilePath,
  );
  writeFileSync(yamlPath, yamlContent, "utf-8");
  if (verbose) {
    process.stderr.write(`Nexus: generated ${yamlPath} (port=${String(httpPort)})\n`);
  }

  // Write nexus-stack.yml (Docker Compose)
  const composePath = join(cwd, "nexus-stack.yml");
  if (!existsSync(composePath)) {
    writeFileSync(composePath, COMPOSE_TEMPLATE, "utf-8");
    if (verbose) {
      process.stderr.write(`Nexus: generated ${composePath}\n`);
    }
  }

  // Write pgvector init SQL
  const sqlPath = join(cwd, "001-enable-pgvector.sql");
  if (!existsSync(sqlPath)) {
    writeFileSync(sqlPath, PGVECTOR_INIT_SQL, "utf-8");
    if (verbose) {
      process.stderr.write(`Nexus: generated ${sqlPath}\n`);
    }
  }
}
