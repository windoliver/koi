/** Default Docker image reference. */
const DEFAULT_IMAGE_REF_STABLE = "ghcr.io/nexi-lab/nexus:stable";
const DEFAULT_IMAGE_REF_EDGE = "ghcr.io/nexi-lab/nexus:edge";

/**
 * Generate the nexus.yaml content string.
 *
 * Matches the format produced by `nexus init --preset <preset>` so that
 * `nexus up` can consume it without modification.
 */
function generateNexusYamlContent(
  nexusPreset: string,
  dataDir: string,
  httpPort: number,
  apiKey: string,
  channel: "stable" | "edge",
  composeFilePath: string,
): string {
  const imageRef = channel === "stable" ? DEFAULT_IMAGE_REF_STABLE : DEFAULT_IMAGE_REF_EDGE;

  return `preset: ${nexusPreset}
data_dir: ${dataDir}
auth: database
tls: false
services:
- nexus
- postgres
- dragonfly
- zoekt
ports:
  http: ${String(httpPort)}
  grpc: ${String(httpPort + 2)}
  postgres: 5432
  dragonfly: 6379
  zoekt: 6070
compose_profiles:
- core
- cache
- search
api_key: ${apiKey}
compose_file: ${composeFilePath}
image_accelerator: cpu
image_ref: ${imageRef}
image_channel: ${channel}
`;
}

